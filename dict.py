#List is a mutable data structure
list = []
list.append("io")
list.append(89)
list.append("rtyyr")
print(list)

#Dictionary
dict = []
dict = {1 : 'Geeks', 6 : 'op', 2 : 'ui'}
print(dict)

#Tuple is a immutable data structure
tup = ("uiu", 9, "yarwd")
print(tup)

#Set data structure
myset = set(['ui', 9, 'iutuy', 9])
print(myset)